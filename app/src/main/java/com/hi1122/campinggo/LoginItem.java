package com.hi1122.campinggo;

public class LoginItem {
    String userID;
    String userPassword;
    String userName;
    String file;

    public LoginItem() {
    }

    public LoginItem(String userID, String userPassword, String userName, String file) {
        this.userID = userID;
        this.userPassword = userPassword;
        this.userName = userName;
        this.file = file;
    }
}
